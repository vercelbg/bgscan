// New creates a new HTTP settings inspector model.
func New(l *layout.Layout, name string) *Model {
	cfg := config.GetHTTP()
	save := func() tea.Cmd { return saveHTTP(l, cfg) }

	m := &Model{
		layout: l,
		name:   name,
		id:     ui.NewComponentID(),
	}

	// Validation helper closure
	valField := func(tmp *config.HTTPConfig, field string) func(string) error {
		return func(v string) error {
			return fieldErr(validate.ValidateHTTP(tmp), field)
		}
	}

	// ─── Easy settings ───
	workers := newIntInput(l, "Enter number of workers", cfg.Workers,
		func(n int) error {
			tmp := *cfg
			tmp.Workers = n
			return valField(&tmp, "Workers")("")
		},
		func(n int) { cfg.Workers = n }, save)

	host := newStringInput(l, "Enter target host", cfg.Host,
		func(v string) error {
			tmp := *cfg
			tmp.Host = v
			return valField(&tmp, "Host")("")
		}, func(v string) { cfg.Host = v }, save)

	serverName := newStringInput(l, "Enter server name (SNI)", cfg.ServerName,
		func(v string) error {
			tmp := *cfg
			tmp.ServerName = v
			return valField(&tmp, "ServerName")("")
		}, func(v string) { cfg.ServerName = v }, save)

	port := newIntInput(l, "Enter HTTP port", cfg.Port,
		func(n int) error {
			tmp := *cfg
			tmp.Port = n
			return valField(&tmp, "Port")("")
		}, func(n int) { cfg.Port = n }, save)

	protocol := selectinput.New(
		l, "Protocol",
		selectinput.WithValue(cfg.Protocol),
		selectinput.WithFocus[string](),
		selectinput.WithOptions(
			huh.NewOption("HTTP", "http"),
			huh.NewOption("HTTPS", "https"),
		),
		selectinput.WithOnSubmit(func(v string) tea.Cmd {
			cfg.Protocol = v
			return save()
		}),
	)

	httpVersion := selectinput.New(
		l, "HTTP Version",
		selectinput.WithValue(cfg.Version),
		selectinput.WithFocus[string](),
		selectinput.WithOptions(
			huh.NewOption("HTTP/1.1", "h1"),
			huh.NewOption("HTTP/2", "h2"),
			huh.NewOption("HTTP/1.1 + HTTP/2", "h1,h2"),
			huh.NewOption("HTTP/3 (QUIC)", "h3"),
		),
		selectinput.WithOnSubmit(func(v string) tea.Cmd {
			cfg.Version = v
			return save()
		}),
	)

	tlsValidation := toggleinput.New(
		l, "TLS Validation",
		toggleinput.WithValue(cfg.TLSValidation),
		toggleinput.WithFocus(),
		toggleinput.WithLabels("Enabled", "Disabled"),
		toggleinput.WithOnSubmit(func(v bool) tea.Cmd {
			cfg.TLSValidation = v
			return save()
		}),
	)

	timeout := newDurationInput(l, "Enter timeout (milliseconds)", cfg.Timeout.Duration(),
		func(d time.Duration) error {
			tmp := *cfg
			tmp.Timeout = config.NewDurationMS(d)
			return valField(&tmp, "Timeout")("")
		},
		func(d time.Duration) { cfg.Timeout = config.NewDurationMS(d) }, save)

	prefixOutput := newStringInput(l, "Enter output file prefix", cfg.PrefixOutput,
		func(v string) error {
			tmp := *cfg
			tmp.PrefixOutput = v
			return valField(&tmp, "PrefixOutput")("")
		}, func(v string) { cfg.PrefixOutput = v }, save)

	// ─── Advanced settings ───
	minTLSVersion := selectinput.New(
		l, "Min TLS Version",
		selectinput.WithValue(cfg.MinTLSVersion),
		selectinput.WithFocus[string](),
		selectinput.WithOptions(
			huh.NewOption("TLS 1.0", "tls1.0"),
			huh.NewOption("TLS 1.1", "tls1.1"),
			huh.NewOption("TLS 1.2", "tls1.2"),
			huh.NewOption("TLS 1.3", "tls1.3"),
		),
		selectinput.WithValidation(func(v string) error {
			tmp := *cfg
			tmp.MinTLSVersion = v
			return valField(&tmp, "MinTLSVersion")("")
		}),
		selectinput.WithOnSubmit(func(v string) tea.Cmd {
			cfg.MinTLSVersion = v
			return save()
		}),
	)

	maxTLSVersion := selectinput.New(
		l, "Max TLS Version",
		selectinput.WithValue(cfg.MaxTLSVersion),
		selectinput.WithFocus[string](),
		selectinput.WithOptions(
			huh.NewOption("TLS 1.0", "tls1.0"),
			huh.NewOption("TLS 1.1", "tls1.1"),
			huh.NewOption("TLS 1.2", "tls1.2"),
			huh.NewOption("TLS 1.3", "tls1.3"),
		),
		selectinput.WithValidation(func(v string) error {
			tmp := *cfg
			tmp.MaxTLSVersion = v
			return valField(&tmp, "MaxTLSVersion")("")
		}),
		selectinput.WithOnSubmit(func(v string) tea.Cmd {
			cfg.MaxTLSVersion = v
			return save()
		}),
	)

	// ─── Status Code Configuration ───
	initGroup := groupStatusCodes(cfg.AcceptedStatusCodes)
	groupMultiselectValue := make([]string, 0, len(initGroup))
	for k := range initGroup {
		groupMultiselectValue = append(groupMultiselectValue, k)
	}

	groupOpts := make([]huh.Option[string], len(groupOrder))
	for i, g := range groupOrder {
		groupOpts[i] = huh.NewOption(groupLabels[g], g)
	}

	statusCodeGroups := multiselect.New(
		l, "Select accepted status code groups",
		multiselect.WithFocus[string](),
		multiselect.WithOptions(groupOpts...),
		multiselect.WithValue(groupMultiselectValue),
		multiselect.WithRequired[string](),
		multiselect.WithOnSubmit(func(selected []string) tea.Cmd {
			if !groupsChanged(groupMultiselectValue, selected) {
				return nil
			}

			cfg.AcceptedStatusCodes = codesFromGroups(selected)
			groupMultiselectValue = selected
			return save()
		}),
	)

	// Build per-group specific code selectors.
	var statusCodeSpecificFields []inspector.Field

	for _, g := range groupOrder {
		group := g

		specific := multiselect.New(
			l,
			fmt.Sprintf("Fine-tune %s codes", groupLabels[group]),
			multiselect.WithFocus[int](),
			multiselect.WithOptions(buildSpecificOptions(group)...),
			multiselect.WithValue(initGroup[g]),
			multiselect.WithRequired[int](),
			multiselect.WithOnSubmit(func(selected []int) tea.Cmd {
				otherGroups := filterCodesByActiveGroups(cfg.AcceptedStatusCodes, groupMultiselectValue)
				cfg.AcceptedStatusCodes = append(otherGroups, selected...)
				return save()
			}),
		)

		statusCodeGroups.AppendOnSubmit(func() tea.Cmd {
			specific.SetValue(filterCodesByGroup(cfg.AcceptedStatusCodes, group))
			return m.refresh()
		})

		// Capture group variable for closure
		currentGroup := group
		visible := func() bool {
			return slices.Contains(groupMultiselectValue, currentGroup)
		}

		statusCodeSpecificFields = append(statusCodeSpecificFields, inspector.Field{
			Name:        fmt.Sprintf("Accepted %s Codes", group),
			Description: fmt.Sprintf("Select which %s codes count as successful.", groupLabels[group]),
			Group:       groupAdvanceHTTPS,
			Input:       inspector.Adapt(specific),
			Visible:     visible,
			Format:      inspector.FormatIntList,
		})
	}

	// Visibility predicates
	isHTTPS := func() bool { return cfg.Protocol == "https" }
	isStrictTLS := func() bool { return isHTTPS() && cfg.TLSValidation && !isHTTP3(cfg.Version) }

	// ─── Field Assembly ───
	fields := []inspector.Field{
		// Easy settings (always visible)
		{Name: "Workers", Description: descWorkers, Group: groupHTTP, Input: inspector.Adapt(workers), Visible: alwaysVisible, Format: inspector.FormatInt},
		{Name: "Host", Description: descHost, Group: groupHTTP, Input: inspector.Adapt(host), Visible: alwaysVisible},
		{Name: "Port", Description: descPort, Group: groupHTTP, Input: inspector.Adapt(port), Visible: alwaysVisible},
		{Name: "Protocol", Description: descProtocol, Group: groupHTTP, Input: inspector.Adapt(protocol), Visible: alwaysVisible},
		{Name: "HTTP Version", Description: descHTTPVersion, Group: groupHTTP, Input: inspector.Adapt(httpVersion), Visible: isHTTPS},
		{Name: "Timeout", Description: descTimeout, Group: groupHTTP, Input: inspector.Adapt(timeout), Visible: alwaysVisible, Format: inspector.FormatDurationMS},
		{Name: "TLS Validation", Description: descTLSValidation, Group: groupHTTP, Input: inspector.Adapt(tlsValidation), Visible: isHTTPS, Format: inspector.FormatBool},

		// Advanced settings
		{Name: "Server Name SNI", Description: descServerName, Group: groupAdvanceHTTPS, Input: inspector.Adapt(serverName), Visible: isHTTPS},
		{Name: "Min TLS Version", Description: descMinTLSVersion, Group: groupAdvanceHTTPS, Input: inspector.Adapt(minTLSVersion), Visible: isStrictTLS},
		{Name: "Max TLS Version", Description: descMaxTLSVersion, Group: groupAdvanceHTTPS, Input: inspector.Adapt(maxTLSVersion), Visible: isStrictTLS},
		{Name: "Prefix Output", Description: descPrefixOutput, Group: groupAdvanceHTTPS, Input: inspector.Adapt(prefixOutput), Visible: alwaysVisible},
		{Name: "Status Code Groups", Description: descStatusCodeGroups, Group: groupAdvanceHTTPS, Input: inspector.Adapt(statusCodeGroups), Visible: alwaysVisible, Format: inspector.FormatStringList},
	}

	fields = append(fields, statusCodeSpecificFields...)

	m.inspector = inspector.New(l, "http settings", fields)
	return m
}

func alwaysVisible() bool { return true }

func fieldErr(errs map[string]error, field string) error {
	if errs == nil {
		return nil
	}
	return errs[field]
}

func isHTTP3(v string) bool {
	return v == "h3" || v == "http3"
}

func (m *Model) refresh() tea.Cmd {
	if m.inspector != nil {
		if ins, ok := m.inspector.(*inspector.Model); ok {
			return ins.Refresh()
		}
	}
	return nil
}
