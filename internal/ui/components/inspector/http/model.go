// Model represents the HTTP settings inspector component.
type Model struct {
	layout    *layout.Layout
	name      string
	id        ui.ComponentID
	inspector ui.Component
}

func (m *Model) ID() ui.ComponentID { return m.id }
func (m *Model) Init() tea.Cmd      { return nil }
func (m *Model) Mode() env.Mode     { return env.NormalMode }
func (m *Model) Name() string       { return m.name }
func (m *Model) OnClose() tea.Cmd   { return nil }

// saveHTTP saves the current HTTP configuration and returns a notification command on error.
func saveHTTP(l *layout.Layout, cfg *config.HTTPConfig) tea.Cmd {
	if err := config.SaveHTTPConfig(cfg); err != nil {
		return notice.NewNoticeCmd(l, "Failed to save HTTP settings", err.Error(), notice.NOTICE_ERROR)
	}
	return nil
}

// newIntInput creates a text input for integer values with validation and auto-save.
func newIntInput(l *layout.Layout, title string, value int, validateFn func(int) error, setFn func(int), saveFn func() tea.Cmd) input.Input[string] {
	return textinput.New(
		l, title,
		textinput.WithValue(strconv.Itoa(value)),
		textinput.WithValidation(func(v string) error {
			n, err := strconv.Atoi(v)
			if err != nil {
				return err
			}
			return validateFn(n)
		}),
		textinput.WithFocus(),
		textinput.WithOnSubmit(func(v string) tea.Cmd {
			n, err := strconv.Atoi(v)
			if err != nil {
				return notice.NewNoticeCmd(l, "Invalid "+title, err.Error(), notice.NOTICE_ERROR)
			}
			setFn(n)
			return saveFn()
		}),
	)
}

// newDurationInput creates a text input for duration values (in ms) with validation and auto-save.
func newDurationInput(l *layout.Layout, title string, value time.Duration, validateFn func(time.Duration) error, setFn func(time.Duration), saveFn func() tea.Cmd) input.Input[string] {
	return textinput.New(
		l, title,
		textinput.WithValue(strconv.FormatInt(value.Milliseconds(), 10)),
		textinput.WithValidation(func(v string) error {
			n, err := strconv.Atoi(v)
			if err != nil {
				return err
			}
			return validateFn(time.Duration(n) * time.Millisecond)
		}),
		textinput.WithFocus(),
		textinput.WithOnSubmit(func(v string) tea.Cmd {
			n, err := strconv.Atoi(v)
			if err != nil {
				return notice.NewNoticeCmd(l, "Invalid "+title, err.Error(), notice.NOTICE_ERROR)
			}
			setFn(time.Duration(n) * time.Millisecond)
			return saveFn()
		}),
	)
}

// newStringInput creates a text input for string values with validation and auto-save.
func newStringInput(l *layout.Layout, title, value string, validateFn func(string) error, setFn func(string), saveFn func() tea.Cmd) input.Input[string] {
	return textinput.New(
		l, title,
		textinput.WithValue(value),
		textinput.WithValidation(validateFn),
		textinput.WithFocus(),
		textinput.WithOnSubmit(func(v string) tea.Cmd {
			setFn(v)
			return saveFn()
		}),
	)
}

// ── Status Code Helpers ────────────────────────────────────────────────

// codesFromGroups expands a list of group names (e.g., ["2xx"]) into a flat list of status codes.
func codesFromGroups(groups []string) []int {
	count := 0
	for _, g := range groups {
		count += len(statusGroups[g])
	}
	out := make([]int, 0, count)
	for _, g := range groups {
		out = append(out, statusGroups[g]...)
	}
	return out
}

// filterCodesByGroup returns only the codes from the input slice that belong to the specified group.
func filterCodesByGroup(codes []int, group string) []int {
	groupCodes := statusGroups[group]
	set := make(map[int]struct{}, len(codes))
	for _, c := range codes {
		set[c] = struct{}{}
	}

	cs := make([]int, 0, 4)
	for _, c := range groupCodes {
		if _, ok := set[c]; ok {
			cs = append(cs, c)
		}
	}
	return cs
}

// groupIsActive checks if any code from the given group exists in the provided list.
func groupIsActive(codes []int, group string) bool {
	set := make(map[int]struct{}, len(codes))
	for _, c := range codes {
		set[c] = struct{}{}
	}
	for _, c := range statusGroups[group] {
		if _, ok := set[c]; ok {
			return true
		}
	}
	return false
}

// groupsChanged compares two slices of group names to detect changes.
func groupsChanged(old, new []string) bool {
	if len(old) != len(new) {
		return true
	}
	oldSet := make(map[string]struct{}, len(old))
	for _, g := range old {
		oldSet[g] = struct{}{}
	}
	for _, g := range new {
		if _, ok := oldSet[g]; !ok {
			return true
		}
	}
	return false
}

// buildSpecificOptions creates huh options for a specific status code group.
func buildSpecificOptions(group string) []huh.Option[int] {
	codes := statusGroups[group]
	opts := make([]huh.Option[int], len(codes))
	for i, c := range codes {
		opts[i] = huh.NewOption(strconv.Itoa(c), c)
	}
	return opts
}

// filterCodesByActiveGroups returns codes that belong to one of the active groups.
func filterCodesByActiveGroups(codes []int, activeGroups []string) []int {
	active := make(map[string]struct{}, len(activeGroups))
	for _, g := range activeGroups {
		active[g] = struct{}{}
	}

	var out []int
	for _, c := range codes {
		for g, groupCodes := range statusGroups {
			if _, ok := active[g]; !ok {
				continue
			}
			if slices.Contains(groupCodes, c) {
				out = append(out, c)
				break
			}
		}
	}
	return out
}

// groupStatusCodes categorizes a flat list of status codes into their respective groups.
// If the list is empty, it returns all standard groups.
func groupStatusCodes(codes []int) map[string][]int {
	if len(codes) == 0 {
		// Return a copy to prevent mutation
		res := make(map[string][]int, len(statusGroups))
		for k, v := range statusGroups {
			res[k] = append([]int(nil), v...)
		}
		return res
	}

	groups := map[string][]int{
		"1xx": {},
		"2xx": {},
		"3xx": {},
		"4xx": {},
		"5xx": {},
	}

	for _, code := range codes {
		switch code / 100 {
		case 1:
			groups["1xx"] = append(groups["1xx"], code)
		case 2:
			groups["2xx"] = append(groups["2xx"], code)
		case 3:
			groups["3xx"] = append(groups["3xx"], code)
		case 4:
			groups["4xx"] = append(groups["4xx"], code)
		case 5:
			groups["5xx"] = append(groups["5xx"], code)
		}
	}

	// Remove empty groups
	for k, v := range groups {
		if len(v) == 0 {
			delete(groups, k)
		}
	}

	return groups
}

